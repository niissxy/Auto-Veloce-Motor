export function getLocalizedVehicle(vehicle: any, lang: SupportedLanguage): any {
  if (!vehicle) return vehicle;
  
  const localized = { ...vehicle, specs: { ...vehicle.specs } };
  
  const trans = VEHICLE_TRANSLATIONS[lang]?.[vehicle.id];
  if (trans) {
    if (trans.name) localized.name = trans.name;
    if (trans.description) localized.description = trans.description;
    if (trans.warranty) localized.warranty = trans.warranty;
    if (trans.branchName) localized.branchName = trans.branchName;
    if (trans.specs) {
      localized.specs = { ...localized.specs, ...trans.specs };
    }
  }

  const t = TRANSLATIONS[lang] || TRANSLATIONS.en;
  if (vehicle.badges) {
    localized.badges = vehicle.badges.map((b: string) => {
      if (b === 'Certified') return t.certified || 'Certified';
      if (b === 'Low KM') return t.lowKM || 'Low KM';
      if (b === 'Luxury Collection') return t.luxury || 'Luxury Collection';
      if (b === 'Electric Vehicle') return t.ev || 'Electric Vehicle';
      if (b === 'New Arrival') return t.newArrival || 'New Arrival';
      if (b === 'Hot Deal') return t.hotDeal || 'Hot Deal';
      if (b === 'Best Seller') return t.bestSeller || 'Best Seller';
      return b;
    });
  }

  if (vehicle.condition) {
    if (vehicle.condition === 'New') {
      localized.displayCondition = lang === 'id' ? 'Baru' : lang === 'zh' ? '全新' : lang === 'ar' ? 'جديد' : lang === 'ja' ? '新車' : 'New';
    } else if (vehicle.condition === 'Used') {
      localized.displayCondition = lang === 'id' ? 'Bekas' : lang === 'zh' ? '二手' : lang === 'ar' ? 'مستعمل' : lang === 'ja' ? '中古車' : 'Used';
    }
  }

  if (vehicle.transmission) {
    if (vehicle.transmission === 'Automatic') {
      localized.displayTransmission = lang === 'id' ? 'Otomatis' : lang === 'zh' ? '自动挡' : lang === 'ar' ? 'أوتوماتيك' : lang === 'ja' ? 'オートマ' : 'Automatic';
    } else if (vehicle.transmission === 'Manual') {
      localized.displayTransmission = lang === 'id' ? 'Manual' : lang === 'zh' ? '手动挡' : lang === 'ar' ? 'يدوي' : lang === 'ja' ? 'マニュアル' : 'Manual';
    } else {
      localized.displayTransmission = vehicle.transmission;
    }
  }

  if (vehicle.fuel) {
    if (vehicle.fuel === 'Petrol') {
      localized.displayFuel = lang === 'id' ? 'Bensin' : lang === 'zh' ? '汽油' : lang === 'ar' ? 'بنزين' : lang === 'ja' ? 'ガソリン' : 'Petrol';
    } else if (vehicle.fuel === 'Diesel') {
      localized.displayFuel = lang === 'id' ? 'Solar' : lang === 'zh' ? '柴油' : lang === 'ar' ? 'ديزل' : lang === 'ja' ? 'ディーゼル' : 'Diesel';
    } else if (vehicle.fuel === 'Electric') {
      localized.displayFuel = lang === 'id' ? 'Listrik' : lang === 'zh' ? '纯电动' : lang === 'ar' ? 'كهربائية' : lang === 'ja' ? '電気' : 'Electric';
    } else if (vehicle.fuel === 'Hybrid') {
      localized.displayFuel = lang === 'id' ? 'Hibrida' : lang === 'zh' ? '油电混合' : lang === 'ar' ? 'هجين' : lang === 'ja' ? 'ハイブリッド' : 'Hybrid';
    }
  }

  return localized;
}

